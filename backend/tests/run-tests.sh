#!/bin/bash

echo "========================================"
echo "  电商系统单元测试"
echo "========================================"
echo ""

cd "$(dirname "$0")"

echo "[1/3] 检查 Node.js 环境..."
node --version || { echo "错误: 未安装 Node.js"; exit 1; }

echo ""
echo "[2/3] 安装测试依赖..."
npm install jest supertest --save-dev || { echo "错误: 依赖安装失败"; exit 1; }

echo ""
echo "[3/3] 运行测试..."
echo ""

npm test || { echo ""; echo "========================================"; echo "  测试失败"; echo "========================================"; exit 1; }

echo ""
echo "========================================"
echo "  所有测试通过！"
echo "========================================"
